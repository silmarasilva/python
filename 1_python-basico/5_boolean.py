# Tipo de dado bool (bolean)
# Ao questionar algo em um programa só existem duas respostas possível sim (true) e falso (false).
# Existem vários operadores para "questionar". 
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro.

print (10 == 10) # True
print (10 == 11) # False

print (type(10 == 10.1)) # <class 'bool'>



