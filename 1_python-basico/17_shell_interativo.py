# O que é o Shell Interativo?
# Um ambiente onde você pode digitar código Python e executar imediatamente.
# Funciona como um "playground" para testar scripts, explorar bibliotecas, ou entender como algo funciona.
# É acessado diretamente no terminal (prompt de comando ou shell do sistema).
# Você digita um comando e vê o resultado na hora:


# 1. Fazer cálculos rápidos
# >>> 5 * 10
# 50
# >>> 100 / 4
# 25.0

# 2. Testar funções
# >>> def soma(a, b):
# ...     return a + b
# ... 
# >>> soma(3, 7)
# 10

# 3. Importar bibliotecas e usá-las
# >>> import math
# >>> math.sqrt(16)
# 4.0
# >>> math.pi
# 3.141592653589793


# 4. Explorar listas e strings
# >>> lista = [1, 2, 3, 4]
# >>> len(lista)
# 4
# >>> "Python".upper()
# 'PYTHON'



